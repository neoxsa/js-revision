// Switch = Use when too much multiple checks

const fruit = "mango"

switch (fruit) {
    case "apple":
        console.log("Apple")
        break;
    case "melon":
        console.log("Melon")
        break;
    case "mango":
        console.log("Mango")
        break;
    case "grapes":
        console.log("Grapes")
        break;
 
    default:
        console.log("Fruit is Mango")
        break;
}